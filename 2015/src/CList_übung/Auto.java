package CList_übung;

public class Auto {
	private String farbe;
	private String knz;
	
	public Auto(String farbe, String knz){
		this.farbe=farbe;
		this.knz=knz;
	}
	public String getFarbe() {
		return farbe;
	}
	public void setFarbe(String farbe) {
		this.farbe = farbe;
	}
	public String getKennzeichen() {
		return knz;
	}
	public void setKennzeichen(String kennzeichen) {
		this.knz = kennzeichen;
	}
	

}
