package OsterUebung;

public class Ei {
	
	private String farbe;
	private int gewicht;
	
	public Ei(int gewicht, String farbe){
		
		this.farbe=farbe;
		this.gewicht=gewicht;
	}

	public String getFarbe() {
		return farbe;
	}

	public int getGewicht() {
		return gewicht;
	}
	
	

}
